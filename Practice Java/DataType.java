public class DataType {
    public static void main(String[] args){
        boolean b;
        float f;
        int n;
        double d;
        short s;
        char c;
        b = true;
        f = 12.5f;
        n = 1200;
        d = 12.45;
        s = 12345;
        c = 'f';

        System.out.println("b :" +b);
        System.out.println("f :" +f);
        System.out.println("n :" +n);
        System.out.println("d :" +d);
        System.out.println("s :" +s);
        System.out.println("c :" +c);

    }
}
